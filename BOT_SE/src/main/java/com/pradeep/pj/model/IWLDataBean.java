package com.pradeep.pj.model;

public class IWLDataBean {
	
	private int id;
	private String crawle_url2;
	private String keyphase;
	private int user_id;
	private int project_id;
	
	
	public IWLDataBean() {
		super();
	}

	
	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getCrawle_url2() {
		return crawle_url2;
	}


	public void setCrawle_url2(String crawle_url2) {
		this.crawle_url2 = crawle_url2;
	}


	public String getKeyphase() {
		return keyphase;
	}


	public void setKeyphase(String keyphase) {
		this.keyphase = keyphase;
	}


	public int getUser_id() {
		return user_id;
	}


	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}


	public int getProject_id() {
		return project_id;
	}


	public void setProject_id(int project_id) {
		this.project_id = project_id;
	}
	
	
	
	

}
